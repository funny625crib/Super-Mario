#include "Main.h"
#include "Float2.h"

//---------------------------------------------------------------------------------
//	コンストラクタ
//---------------------------------------------------------------------------------
Float2::Float2()
{
	x = 0.0f;
	y = 0.0f;
}
Float2::Float2( float x, float y )
{
	this->x = x;
	this->y = y;
}
//---------------------------------------------------------------------------------
//	ゼロ初期化
//---------------------------------------------------------------------------------
void Float2::Clear()
{
	x = 0.0f;
	y = 0.0f;
}
//---------------------------------------------------------------------------------
//	値のセット
//---------------------------------------------------------------------------------
void Float2::Set( float x, float y )
{
	this->x = x;
	this->y = y;
}
//---------------------------------------------------------------------------------
//	値のセット
//---------------------------------------------------------------------------------
void Float2::Set( Float2 &f2 )
{
	x = f2.x;
	y = f2.y;
}
//---------------------------------------------------------------------------------
//	長さ取得
//---------------------------------------------------------------------------------
float Float2::GetLength()
{
	return sqrtf( x * x + y * y );
}
//---------------------------------------------------------------------------------
//	正規化（ベクトルの長さを１にする）
//---------------------------------------------------------------------------------
void Float2::Normalize()
{
	float length = GetLength();
	if( length > 0.0f ){
		x = x / length;
		y = y / length;
	}
}
//---------------------------------------------------------------------------------
//	長さ設定
//---------------------------------------------------------------------------------
void Float2::SetLength( float length )
{
	Normalize();
	x = x * length;
	y = y * length;
}

//---------------------------------------------------------------------------------
//	= 演算子のオーバーロード
//---------------------------------------------------------------------------------
Float2& Float2::operator = ( const Float2 &f2 )
{
	x = f2.x;
	y = f2.y; 
	return *this; 
}
//---------------------------------------------------------------------------------
//	+= 演算子のオーバーロード
//---------------------------------------------------------------------------------
Float2& Float2::operator += ( const Float2 &f2 )
{
	x += f2.x;
	y += f2.y;
	return *this;
}
//---------------------------------------------------------------------------------
//	-= 演算子のオーバーロード
//---------------------------------------------------------------------------------
Float2& Float2::operator -= ( const Float2 &f2 )
{
	x -= f2.x;
	y -= f2.y;
	return *this;
}
//---------------------------------------------------------------------------------
//	*= 演算子のオーバーロード
//---------------------------------------------------------------------------------
Float2& Float2::operator *= ( const float f )
{
	x *= f;
	y *= f;
	return *this;
}
//---------------------------------------------------------------------------------
//	/= 演算子のオーバーロード
//---------------------------------------------------------------------------------
Float2& Float2::operator /= ( const float f )
{
	x /= f;
	y /= f;
	return *this;
}

//---------------------------------------------------------------------------------
//	+ 演算子のオーバーロード
//---------------------------------------------------------------------------------
Float2 operator + ( const Float2 &f2_1, const Float2 &f2_2 )
{
	Float2 ret;
	ret.x = f2_1.x + f2_2.x;
	ret.y = f2_1.y + f2_2.y;
	return ret;
}
//---------------------------------------------------------------------------------
//	- 演算子のオーバーロード
//---------------------------------------------------------------------------------
Float2 operator - ( const Float2 &f2_1, const Float2 &f2_2 )
{
	Float2 ret;
	ret.x = f2_1.x - f2_2.x;
	ret.y = f2_1.y - f2_2.y;
	return ret;
}
//---------------------------------------------------------------------------------
//	* 演算子のオーバーロード
//---------------------------------------------------------------------------------
Float2 operator * ( const Float2 &f2, const float f )
{
	Float2 ret;
	ret.x = f2.x * f;
	ret.y = f2.y * f;
	return ret;
}
//---------------------------------------------------------------------------------
//	/ 演算子のオーバーロード
//---------------------------------------------------------------------------------
Float2 operator / ( const Float2 &f2, const float f )
{
	Float2 ret;
	ret.x = f2.x / f;
	ret.y = f2.y / f;
	return ret;
}

//---------------------------------------------------------------------------------
//	マウス座標を Float2 で取得
//---------------------------------------------------------------------------------
Float2 GetMouseFloat2()
{
	Float2 pos;
	pos.x = (float)GetMouseX();
	pos.y = (float)GetMouseY();
	return pos;
}
//---------------------------------------------------------------------------------
//	２つの Float2 の距離を求める
//---------------------------------------------------------------------------------
float GetFloat2Distance( Float2 &pos1, Float2 &pos2 )
{
	float x = pos1.x - pos2.x;
	float y = pos1.y - pos2.y;
	float distance = sqrtf( x * x + y * y );
	return distance;
}
//---------------------------------------------------------------------------------
//	２つの Float2 の内積を求める
//---------------------------------------------------------------------------------
float GetFloat2Dot( Float2 &v1, Float2 &v2 )
{
	return v1.x * v2.x + v1.y * v2.y;
}
//---------------------------------------------------------------------------------
//	２つの Float2 の外積を求める
//---------------------------------------------------------------------------------
float GetFloat2Cross( Float2 &v1, Float2 &v2 )
{
	return v1.x * v2.y - v1.y * v2.x;
}
